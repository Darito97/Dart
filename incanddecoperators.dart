void main() {
  int num1 = 0;
  int num2 = 0;

  num2 = ++num1;
  print("El valor de num2 es $num2");

  num1 = 0;
  num2 = 0;

  num2 = num1++;
  print("El valor de num2 es $num2");

  num1 = 0;
  num2 = 0;

  num2 = --num1;
  print("El valor de num2 es $num2");

  num1 = 0;
  num2 = 0;

  num2 = num1--;
  print("El valor de num2 es $num2");
}
